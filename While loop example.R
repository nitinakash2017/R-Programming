#While loop example

#logical operator FALSE
while(FALSE){
  print("Hello Nitin")
}

#logical operator TRUE
#Press Escape to terminate infinite loop.
while(TRUE){
  print("Hello Nitin")
}


#Real example
counter <- 1

while(counter < 12) {
  print(counter)
  counter <- counter + 1
}

